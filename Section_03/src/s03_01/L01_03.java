package s03_01;

/**
 * 원시 자료형과 참조 자료형
 * Java에서 변수의 자료형은 크게 원시형(Primitive type)과 참조형(Reference type)으로 나누어진다.
 */

public class L01_03 {
    public static void main(String[] args) {
        // 원시 자료형
        int age = 32;
        double height = 175.6;
        boolean hasJob = true;
        char sex = 'M';
        // 참조 자료형
        String name = "Thomas";
    }
}
