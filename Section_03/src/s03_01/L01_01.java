package s03_01;

/**
 * 리터럴 (Literal)
 * 리터럴 또는 리터럴 상수는 값 자체를 의미한다.
 * ✅ Java에서는 리터럴 상수를 선언할 수 없다.
 */

public class L01_01 {
    public static void main(String[] args) {
        // 리터럴의 형태
        // 1) 정수 리터럴
        System.out.println(32);
        // 2) 실수 리터럴
        System.out.println(175.6);
        // 3) 논리 리터럴
        System.out.println(true);
        // 4) 문자 리터럴
        System.out.println('M');
        // 5) 문자열 리터럴
        System.out.println("Thomas");
    }
}
