package s03_01;

/**
 * 변수의 자료형
 * 변수를 선언할 때 명시하는 자료형은 변수가 다룰 리터럴의 형태를 의미한다.
 */

public class L01_02 {
    public static void main(String[] args) {
        // 변수의 자료형
        // 1) 정수형
        int age = 32;
        // 2) 실수형
        double height = 175.6;
        // 3) 논리형
        boolean hasJob = true;
        // 4) 문자형
        char sex = 'M';
        // 5) 문자열형
        String name = "Thomas";
    }
}
